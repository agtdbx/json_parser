{

}


////////////////////////////////////////////////////////////////////////////////
// Getters
////////////////////////////////////////////////////////////////////////////////
JsonContent	&Json::getContent(std::string &key)
{
	JsonContent	*content = NULL;
	try
	{
		content = &this->data.at(key);
	}
	catch (std::exception e)
	{
		throw JsonKeyError();
	}
	return (*content);
}


