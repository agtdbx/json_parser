{

}


////////////////////////////////////////////////////////////////////////////////
// Operators
////////////////////////////////////////////////////////////////////////////////
Json	&Json::operator=(Json const &json)
{
	if (this == &json)
		return (*this);

	this->data = json.data;

	return (*this);
}


